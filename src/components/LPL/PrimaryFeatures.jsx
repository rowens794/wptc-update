function FeaturesMobile() {
  let slideContainerRef = useRef()
  let slideRefs = useRef([])

  useEffect(() => {
    let observer = new window.IntersectionObserver(
      (entries) => {
        for (let entry of entries) {
          if (entry.isIntersecting) {
            break
          }
        }
      },
      {
        root: slideContainerRef.current,
        threshold: 0.6,
      }
    )

    for (let slide of slideRefs.current) {
      if (slide) {
        observer.observe(slide)
      }
    }

    return () => {
      observer.disconnect()
    }
  }, [slideContainerRef, slideRefs])
  return (
    <>
      <div
        ref={slideContainerRef}
        className="-mb-4 flex snap-x snap-mandatory -space-x-4 overflow-x-auto overscroll-x-contain scroll-smooth pb-4 [scrollbar-width:none] sm:-space-x-6 [&::-webkit-scrollbar]:hidden"
      >
        {features.map((feature, featureIndex) => {
          return (
            <div
              key={featureIndex}
              ref={(ref) => (slideRefs.current[featureIndex] = ref)}
              className="w-full flex-none snap-center px-4 sm:px-6"
            >
              <div
                className={`relative h-[300px] transform overflow-hidden rounded-2xl bg-gray-800 bg-cover px-5 py-6 `}
                style={{ backgroundImage: `url(${feature.img})` }}
              >
                <div className="absolute inset-x-0 bottom-0 h-full bg-gray-800 p-6 opacity-90">
                  <feature.icon className="h-8 w-8" />
                  <h3 className="mt-6 text-sm font-semibold text-white sm:text-lg">
                    {feature.name}
                  </h3>
                  <p className="mt-2 text-sm text-gray-400">
                    {feature.description}
                  </p>
                </div>
              </div>
            </div>
          )
        })}
      </div>
      <div className="mt-6 flex justify-center gap-3">
        {features.map((feature, featureIndex) => (
          <button
            type="button"
            key={featureIndex}
            className=""
            onClick={() => {
              slideRefs.current[featureIndex].scrollIntoView({
                block: 'nearest',
                inline: 'nearest',
              })
            }}
          >
            <span className="text-xs text-white"> {feature.shortDesc}</span>
          </button>
        ))}
      </div>
    </>
  )
}

